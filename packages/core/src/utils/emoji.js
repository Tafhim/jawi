/**
 * Convert emoji shortcodes in text to Unicode emoji characters.
 *
 * Matches `:shortcode:` patterns (case-insensitive) and replaces
 * them with the corresponding emoji from EMOJI_MAP.
 *
 * @param {string} text - The markdown text to process
 * @returns {string} - Text with emoji shortcodes replaced by Unicode characters
 *
 * @example
 * convertEmojis('I love coding :rocket: :heart:')
 * // Returns 'I love coding \u{1F680} \u{2764}'
 */
export function convertEmojis(text) {
  // Match :word: patterns (word characters and underscores)
  return text.replace(/:([a-z0-9_]+):/gi, (match, name) => {
    const lower = name.toLowerCase();
    return EMOJI_MAP[lower] !== undefined ? EMOJI_MAP[lower] : match;
  });
}
